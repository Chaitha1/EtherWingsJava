package basicjava;

public class EscapeSequence {

	public static void main(String[] args) {
		
		        // Demonstrating the use of \n (Newline)
		        System.out.println("Line 1\nLine 2");

		        // Demonstrating the use of \t (Tab)
		        System.out.println("Column1\tColumn2\tColumn3");

		        // Demonstrating the use of \\ (Backslash)
		        System.out.println("This is a backslash: \\");

		        // Demonstrating the use of \" (Double quote)
		        System.out.println("He said, \"Hello, World!\"");

		        // Demonstrating the use of \' (Single quote)
		        System.out.println("It\'s a lovely day!");

		        // Demonstrating the use of \r (Carriage return)
		        System.out.print("Hello\rJava\n");

		        // Demonstrating the use of \b (Backspace)
		        System.out.println("Oops, there was a mistake\b\b\b\b!"); // removes "ake"

		        // Demonstrating the use of \f (Form feed)
		        System.out.println("This is before the form feed.\fThis is after the form feed.");
		    }
		


	}


